module.exports.tambah = (angka1, angka2) => {
    return angka1 + angka2;
}

module.exports.kurang = (angka1, angka2) => {
    return angka1 - angka2;
}

module.exports.kali = (angka1, angka2) => {
    return angka1 * angka2;
}

module.exports.bagi = (angka1, angka2) => {
    return angka1 / angka2;
}